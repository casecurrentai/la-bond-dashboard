export function getCallStats() {
  const today = new Date("2026-03-30");
  const todayCalls = MOCK_CALLS.filter(c => c.timestamp.startsWith("2026-03-30"));
  const qualified = todayCalls.filter(c => c.decision === "QUALIFIED").length;
  const transferred = todayCalls.filter(c => c.transferStatus === "transferred").length;
  const avgPremium = todayCalls
    .filter(c => c.premium !== null)
    .reduce((sum, c) => sum + (c.premium ?? 0), 0) /
    (todayCalls.filter(c => c.premium !== null).length || 1);

  return {
    today: {
      totalCalls: todayCalls.length,
      qualified,
      unqualified: todayCalls.filter(c => c.decision === "UNQUALIFIED").length,
      paymentPlan: todayCalls.filter(c => c.decision === "PAYMENT_PLAN_ELIGIBLE").length,
      needsReview: todayCalls.filter(c => c.decision === "NEEDS_MANUAL_REVIEW").length,
      transferred,
      avgPremium: Math.round(avgPremium),
      totalRevenuePotential: todayCalls.reduce((sum, c) => sum + (c.premium ?? 0), 0),
    },
    allTime: {
      totalCalls: MOCK_CALLS.length,
      qualified: MOCK_CALLS.filter(c => c.decision === "QUALIFIED").length,
      transferred: MOCK_CALLS.filter(c => c.transferStatus === "transferred").length,
    },
  };
}
